    private void doSave(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doSave
        // If the user clicks on the Save button, we are going to check to see
        //+ if our dirty flag is set. If not, there is no need to save, so we
        //+ will just close the dialog.
        if ( this.isDirty ) {
            props.setDataFolder(this.defaultDataFolder.getText());
            props.setProperty("app.home", this.appHomePath.getText());
            props.setProperty("logs.home", this.logsLocation.getText());
            props.setProperty("err.home", this.errLogLocation.getText());
            props.setProperty("db.name", this.defaultDBName.getText());
            props.setProperty("db.driver", 
                    this.dbDriver.getSelectedItem().toString());
            props.setProperty("db.url", this.dbDriverConnURL.getText());
            props.setProperty("db.options", 
                    this.dbConnOptions.getText());
            this.isDirty = false;
        }
        
        doCancel(evt);
    }//GEN-LAST:event_doSave

