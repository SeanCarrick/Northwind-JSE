    private void doCancel(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doCancel
        // Close the window without saving the changes, if any, but prompt the
        //+ user first if changes were made.
        if ( this.isDirty ) {
            String msg = "Do you want to save your changes?";
            String ttl = "Confirm Cancel";
            int opt = JOptionPane.showConfirmDialog(this, msg, ttl, 
                    JOptionPane.YES_NO_OPTION);
            if ( opt == JOptionPane.YES_OPTION )
            
            doSave(evt);
        }
        this.dispose();
    }//GEN-LAST:event_doCancel

    private void doBrowse(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doBrowse
        // We are going to handle showing the File browser for all browse 
        //+ buttons in this event. We will use their ActionCommand value to
        //+ determine:
        //+     
        //+     A) The title of the file chooser dialog
        //+     B) which text field to update
        String which = evt.getActionCommand();
        
        String dlgTitle = "Choose Folder";
        
        if ( which.equalsIgnoreCase("apphome") )
            dlgTitle = "Choose Northwind Home Folder";
        else if ( which.equalsIgnoreCase("datahome") )
            dlgTitle = "Choose Northwind Data Folder";
        else if ( which.equalsIgnoreCase("logshome") )
            dlgTitle = "Choose Northwind Logs Folder";
        else if ( which.equalsIgnoreCase("errhome") )
            dlgTitle = "Choose Northwind Error Reports Folder";
        else if ( which.equalsIgnoreCase("dbfilehome") )
            dlgTitle = "Choose Location for Data Store File";
        
        JFileChooser choose = new JFileChooser();
        choose.setDialogTitle(dlgTitle);
        choose.setCurrentDirectory(new File(System.getProperty("user.home")));
        
        if ( !which.equalsIgnoreCase("dbfilehome") )
            choose.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        else
            choose.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        
        choose.setMultiSelectionEnabled(false);
        int option = choose.showDialog(this, "Select");
        
        if ( option == JFileChooser.APPROVE_OPTION ) {
            
            File file = choose.getSelectedFile();
            
            if ( which.equalsIgnoreCase("apphome") )
                this.appHomePath.setText(file.getAbsolutePath() 
                        + File.separator);
            else if ( which.equalsIgnoreCase("datahome") )
                this.defaultDataFolder.setText(file.getAbsolutePath() 
                        + File.separator);
            else if ( which.equalsIgnoreCase("logshome") )
                this.logsLocation.setText(file.getAbsolutePath() 
                        + File.separator);
            else if ( which.equalsIgnoreCase("errhome") )
                this.errLogLocation.setText(file.getAbsolutePath()
                        + File.separator);
            else if ( which.equalsIgnoreCase("dbfilehome") )
                this.errLogLocation.setText(file.getAbsolutePath());
            
            this.isDirty = true;
        }
        
        choose = null;
        
    }//GEN-LAST:event_doBrowse

